    public void intializeList() {
        jList1.removeAll();
        DefaultListModel model = new DefaultListModel();
        ArrayList<String> temp = q.readSQL();
        for (int i = 0; i < temp.size(); i++) {
            model.addElement(temp.get(i));
        }
        jList1.setModel(model);
        q.readSQL().clear();

    }

